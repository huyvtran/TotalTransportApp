import {Injectable} from '@angular/core';
import {Headers, Http, RequestOptions} from '@angular/http';
import 'rxjs/add/operator/map';
import {AppConfig} from "../../app/app.config";

/*
  委托Service
*/
@Injectable()
export class CommissionServiceProvider {
  //封装Header提交表单数据
  private header: Headers = new Headers({
    'Content-Type': 'application/x-www-form-urlencoded'
  });
  private options: RequestOptions = new RequestOptions({headers: this.header});

  constructor(public http: Http) {
  }

  //委托信息分页查询
  getBusinessDelegationListByPage(businessDelegation, page, rows) {
    let url = AppConfig.getUrl() + '/app/businessDelegation/getBusinessDelegationListByPage.do';
    let params = {creator: businessDelegation.creator, state: businessDelegation.state, page: page, rows: rows};
    return new Promise((resolve, reject) => {
      this.http.post(url, this.toQueryString(params), this.options)
        .map(res => res.json())
        .subscribe(data => {
          if (Boolean(data)) {
            if (data.result == 'success') {
              let result = {
                result: data.result,
                message: data.message,
                pager: data.pager
              };
              resolve(result);
            } else {
              reject(data.message);
            }
          } else {
            reject('服务器未响应');
          }
        }, err => {
          console.log(err);
          reject('服务器异常,请检查!');
        })
    });
  }

  //发起委托
  addBusinessDelegation(businessDelegation) {
    let url = AppConfig.getUrl() + '/app/businessDelegation/addBusinessDelegation.do';
    let params = {
      cargoType: businessDelegation.cargoType,
      cargoName: businessDelegation.cargoName,
      amount: businessDelegation.amount,
      unit: businessDelegation.unit,
      startDate: businessDelegation.startDate,
      floatDay: businessDelegation.floatDay,
      startPlace: businessDelegation.startPlace,
      endPlace: businessDelegation.endPlace,
      linkMan: businessDelegation.linkMan,
      linkPhone: businessDelegation.linkPhone,
      creator: businessDelegation.creator,
      content: businessDelegation.content,
      clientId: businessDelegation.clientId
    };
    return new Promise((resolve, reject) => {
      this.http.post(url, this.toQueryString(params), this.options)
        .map(res => res.json())
        .subscribe(data => {
          if (Boolean(data)) {
            if (data.result == 'success') {
              let result = {
                result: data.result,
                message: data.message
              };
              resolve(result);
            } else {
              reject(data.message);
            }
          } else {
            reject('服务器未响应');
          }
        }, err => {
          console.log(err);
          reject('服务器异常,请检查!');
        })
    });
  }

  //委托详情查询
  getBusinessDelegationById(id) {
    let url = AppConfig.getUrl() + '/app/businessDelegation/getBusinessDelegationById.do';
    let params = {id: id};
    return new Promise((resolve, reject) => {
      this.http.post(url, this.toQueryString(params), this.options)
        .map(res => res.json())
        .subscribe(data => {
          if (Boolean(data)) {
            if (data.result == 'success') {
              let result = {
                result: data.result,
                message: data.message,
                businessDelegation: data.businessDelegation
              };
              resolve(result);
            } else {
              reject(data.message);
            }
          } else {
            reject('服务器未响应');
          }
        }, err => {
          console.log(err);
          reject('服务器异常,请检查!');
        })
    });
  }


  //参数序列化
  private toQueryString(obj) {
    let result = [];
    for (let key in obj) {
      key = encodeURIComponent(key);
      let values = obj[key];
      if (values && values.constructor == Array) {
        let queryValues = [];
        for (let i = 0, len = values.length, value; i < len; i++) {
          value = values[i];
          queryValues.push(this.toQueryPair(key, value));
        }
        result = result.concat(queryValues);
      } else {
        result.push(this.toQueryPair(key, values));
      }
    }
    return result.join('&');
  }

  //参数序列化
  private toQueryPair(key, value) {
    if (typeof value == 'undefined') {
      return key;
    }
    return key + '=' + encodeURIComponent(value === null ? '' : String(value));
  }


}
